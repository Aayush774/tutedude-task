# Takes a user's first and last name as input.
first_name= str(input("Enter your first name: "))
last_name = str(input("Enter your last name: "))

# Concatenate first and last name to create a full name
full_name = first_name + " " + last_name 

# print a personalized greeting message using the full name
print("\n" "Hello, " + full_name + "! Welcome to the python program.")